package com.colabora.soluciones.convocatoriafeyac.Modelos.paginaWebAplicaciones;

public class serviciosAplicacion {

    private String imagen;
    private String serviciocuatro;
    private String serviciodos;
    private String serviciotres;
    private String serviciuno;
    private String subtitulocuatro;
    private String subtitulodos;
    private String subtitulotres;
    private String subtitulouno;
    private String titulo;

    public String getImagen() {
        return imagen;
    }

    public void setImagen(String imagen) {
        this.imagen = imagen;
    }

    public String getServiciocuatro() {
        return serviciocuatro;
    }

    public void setServiciocuatro(String serviciocuatro) {
        this.serviciocuatro = serviciocuatro;
    }

    public String getServiciodos() {
        return serviciodos;
    }

    public void setServiciodos(String serviciodos) {
        this.serviciodos = serviciodos;
    }

    public String getServiciotres() {
        return serviciotres;
    }

    public void setServiciotres(String serviciotres) {
        this.serviciotres = serviciotres;
    }

    public String getServiciuno() {
        return serviciuno;
    }

    public void setServiciuno(String serviciuno) {
        this.serviciuno = serviciuno;
    }

    public String getSubtitulocuatro() {
        return subtitulocuatro;
    }

    public void setSubtitulocuatro(String subtitulocuatro) {
        this.subtitulocuatro = subtitulocuatro;
    }

    public String getSubtitulodos() {
        return subtitulodos;
    }

    public void setSubtitulodos(String subtitulodos) {
        this.subtitulodos = subtitulodos;
    }

    public String getSubtitulotres() {
        return subtitulotres;
    }

    public void setSubtitulotres(String subtitulotres) {
        this.subtitulotres = subtitulotres;
    }

    public String getSubtitulouno() {
        return subtitulouno;
    }

    public void setSubtitulouno(String subtitulouno) {
        this.subtitulouno = subtitulouno;
    }

    public String getTitulo() {
        return titulo;
    }

    public void setTitulo(String titulo) {
        this.titulo = titulo;
    }

    public serviciosAplicacion() {
    }

    public serviciosAplicacion(String imagen, String serviciocuatro, String serviciodos, String serviciotres, String serviciuno, String subtitulocuatro, String subtitulodos, String subtitulotres, String subtitulouno, String titulo) {
        this.imagen = imagen;
        this.serviciocuatro = serviciocuatro;
        this.serviciodos = serviciodos;
        this.serviciotres = serviciotres;
        this.serviciuno = serviciuno;
        this.subtitulocuatro = subtitulocuatro;
        this.subtitulodos = subtitulodos;
        this.subtitulotres = subtitulotres;
        this.subtitulouno = subtitulouno;
        this.titulo = titulo;
    }
}
